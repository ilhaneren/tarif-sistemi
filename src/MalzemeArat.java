    private void dbConnect() {
        try {
            String url = "jdbc:mysql://localhost:3306/jdbcdemo";
            String user = "root";
            String password = "YOUR_PASSWORD";
            conn = DriverManager.getConnection(url, user, password);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Veritabanına bağlanılamadı: " + e.getMessage());
        }
    }
    
    private void malzemeleriGetir() {
        DefaultTableModel model = (DefaultTableModel) malzemetablo.getModel();
        model.setRowCount(0); 

        try {
            String query = "SELECT * FROM malzemeler";
            pst = conn.prepareStatement(query);
            rs = pst.executeQuery();

            while (rs.next()) {
                Object[] row = { 
                    rs.getInt("malzemeid"), 
                    rs.getString("malzemeadi"), 
                    rs.getDouble("toplammiktar"), 
                    rs.getString("malzemebirim"),
                    rs.getDouble("birimfiyat")
                };
                model.addRow(row);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Malzemeler getirilemedi: " + e.getMessage());
        }
    }
    
     private void malzemeleriFiltrele(String aramaTerimi) {
        DefaultTableModel model = (DefaultTableModel) malzemetablo.getModel();
        model.setRowCount(0); // Tabloyu sıfırla

        try {
            String query = "SELECT * FROM malzemeler WHERE malzemeadi LIKE ? OR malzemebirim LIKE ?";
            pst = conn.prepareStatement(query);
            pst.setString(1, "%" + aramaTerimi + "%");
            pst.setString(2, "%" + aramaTerimi + "%");
            rs = pst.executeQuery();

            while (rs.next()) {
                Object[] row = { 
                    rs.getInt("malzemeid"), 
                    rs.getString("malzemeadi"), 
                    rs.getDouble("toplammiktar"), 
                    rs.getString("malzemebirim"),
                    rs.getDouble("birimfiyat")
                };
                model.addRow(row);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Malzemeler filtrelenemedi: " + e.getMessage());
        }
    }
