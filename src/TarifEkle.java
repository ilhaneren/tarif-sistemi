    private void tarifleriYukle() {
        DefaultComboBoxModel<String> model1 = new DefaultComboBoxModel<>();
        DefaultComboBoxModel<String> model2 = new DefaultComboBoxModel<>();

        try {
            String query = "SELECT malzemeadi FROM malzemeler";
            pst = conn.prepareStatement(query);
            rs = pst.executeQuery();

            while (rs.next()) {
                String malzemeAdi = rs.getString("malzemeadi");
                model1.addElement(malzemeAdi);
                model2.addElement(malzemeAdi);
            }

            cmbmalzeme1.setModel(model1);
            cmbmalzeme2.setModel(model2);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Tarifler yüklenemedi: " + e.getMessage());
        }

        cmbmalzeme1.setSelectedIndex(-1); 
        cmbmalzeme2.setSelectedIndex(-1); 
    }

