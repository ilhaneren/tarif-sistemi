    private void eklebutonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eklebutonActionPerformed
        String queryInsert = "INSERT INTO tarifler (tarifid, tarifadi, kategori, hazirlamasuresi, talimatlar) VALUES (?, ?, ?, ?, ?)";
    String queryUpdate = "UPDATE tarifler SET tarifadi=?, kategori=?, hazirlamasuresi=?, talimatlar=? WHERE tarifid=?";
    String queryTarifMalzeme = "INSERT INTO tarifmalzeme1 (tarifid, malzemeid, miktar) VALUES (?, ?, ?)";
    String queryGetMalzemeId = "SELECT malzemeid FROM malzemeler WHERE malzemeadi = ?";

    int id = Integer.parseInt(tarifidtext.getText());
    String ad = tarifaditext.getText();
    String kategori = kategoritext.getText();
    String hazirlanmasuresi = hazirlanmasuresitext.getText();
    String talimatlar = talimatlartext.getText();

    String malzemeAdi1 = cmbmalzeme1.getSelectedItem().toString();
    String malzemeAdi2 = cmbmalzeme2.getSelectedItem().toString();
    int miktar1 = Integer.parseInt(malzeme1.getText());
    int miktar2 = Integer.parseInt(malzeme2.getText());

    try {
        String checkQuery = "SELECT COUNT(*) FROM tarifler WHERE tarifid = ?";
        PreparedStatement checkStm = conn.prepareStatement(checkQuery);
        checkStm.setInt(1, id);
        ResultSet rs = checkStm.executeQuery();
        
        if (rs.next() && rs.getInt(1) > 0) { 
            PreparedStatement updateStm = conn.prepareStatement(queryUpdate);
            updateStm.setString(1, ad);
            updateStm.setString(2, kategori);
            updateStm.setString(3, hazirlanmasuresi);
            updateStm.setString(4, talimatlar);
            updateStm.setInt(5, id);
            updateStm.executeUpdate();
            JOptionPane.showMessageDialog(this, "Tarif güncellendi.");
        } else { 
            PreparedStatement insertStm = conn.prepareStatement(queryInsert);
            insertStm.setInt(1, id);
            insertStm.setString(2, ad);
            insertStm.setString(3, kategori);
            insertStm.setString(4, hazirlanmasuresi);
            insertStm.setString(5, talimatlar);
            insertStm.execute();
            JOptionPane.showMessageDialog(this, "Yeni tarif eklendi.");
        }

        PreparedStatement getMalzemeIdStm1 = conn.prepareStatement(queryGetMalzemeId);
        getMalzemeIdStm1.setString(1, malzemeAdi1);
        ResultSet rs1 = getMalzemeIdStm1.executeQuery();
        if (rs1.next()) {
            int malzemeid1 = rs1.getInt("malzemeid");
            PreparedStatement tarifMalzemeStm1 = conn.prepareStatement(queryTarifMalzeme);
            tarifMalzemeStm1.setInt(1, id);
            tarifMalzemeStm1.setInt(2, malzemeid1);
            tarifMalzemeStm1.setInt(3, miktar1);
            tarifMalzemeStm1.executeUpdate();
        }

        PreparedStatement getMalzemeIdStm2 = conn.prepareStatement(queryGetMalzemeId);
        getMalzemeIdStm2.setString(1, malzemeAdi2);
        ResultSet rs2 = getMalzemeIdStm2.executeQuery();
        if (rs2.next()) {
            int malzemeid2 = rs2.getInt("malzemeid");
            PreparedStatement tarifMalzemeStm2 = conn.prepareStatement(queryTarifMalzeme);
            tarifMalzemeStm2.setInt(1, id);
            tarifMalzemeStm2.setInt(2, malzemeid2);
            tarifMalzemeStm2.setInt(3, miktar2);
            tarifMalzemeStm2.executeUpdate();
        }

        tarifidtext.setText(null);
        tarifaditext.setText(null);
        kategoritext.setText(null);
        hazirlanmasuresitext.setText(null);
        talimatlartext.setText(null);
        malzeme1.setText(null);
        malzeme2.setText(null);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Hata: " + e.getMessage());
    }

    }//GEN-LAST:event_eklebutonActionPerformed

